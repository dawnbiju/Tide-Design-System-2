/**
 * sync-figma-tokens.mjs
 * ─────────────────────
 * Fetches design tokens (Variables) from the Tide Figma file and writes:
 *   • src/tokens/tokens.ts   — TypeScript token object
 *   • src/styles/globals.css — CSS custom properties
 *
 * Usage:
 *   FIGMA_TOKEN=<pat> FIGMA_FILE_KEY=<key> node scripts/sync-figma-tokens.mjs
 *
 * The script is intentionally dependency-free (uses Node built-ins only).
 */

import { writeFileSync, mkdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'
import https from 'https'

const __dirname  = dirname(fileURLToPath(import.meta.url))
const ROOT       = join(__dirname, '..')
const TOKEN      = process.env.FIGMA_TOKEN
const FILE_KEY   = process.env.FIGMA_FILE_KEY || 'TrWgWyHmqjNcjSURuXJmnm'

if (!TOKEN) {
  console.error('❌  FIGMA_TOKEN environment variable is required.')
  process.exit(1)
}

/** Simple HTTPS GET → JSON */
function get(url) {
  return new Promise((resolve, reject) => {
    const req = https.get(url, { headers: { 'X-Figma-Token': TOKEN } }, res => {
      let body = ''
      res.on('data', d => body += d)
      res.on('end', () => {
        try { resolve(JSON.parse(body)) }
        catch (e) { reject(e) }
      })
    })
    req.on('error', reject)
  })
}

/** Convert Figma RGBA {r,g,b,a} → #RRGGBB */
function toHex({ r, g, b }) {
  return '#' + [r, g, b].map(c => Math.round(c * 255).toString(16).padStart(2, '0')).join('').toUpperCase()
}

/** Map Figma variable name → CSS custom property name */
function toCssVar(name) {
  return '--' + name.toLowerCase().replace(/[\/\s]+/g, '-').replace(/[^a-z0-9-]/g, '')
}

async function main() {
  console.log(`🔄  Fetching variables from Figma file ${FILE_KEY}…`)

  const data = await get(`https://api.figma.com/v1/files/${FILE_KEY}/variables/local`)

  if (data.status === 403) {
    console.error('❌  Access denied. Check that your FIGMA_TOKEN has read access.')
    process.exit(1)
  }

  const variables  = Object.values(data.meta?.variables  ?? {})
  const collections = Object.values(data.meta?.variableCollections ?? {})

  if (variables.length === 0) {
    console.warn('⚠️   No variables found — Figma file may not publish variables publicly.')
    console.log('ℹ️   Writing fallback tokens from design inspection data…')
    writeFallback()
    return
  }

  // ── Build a flat token map ─────────────────────────────────────────────────
  const tokens = {}
  for (const v of variables) {
    const col  = collections.find(c => c.id === v.variableCollectionId)
    const mode = Object.values(col?.modes ?? {})[0]
    const val  = v.valuesByMode?.[mode?.modeId]
    if (!val) continue

    const cssVar = toCssVar(v.name)
    tokens[cssVar] = typeof val === 'object' && 'r' in val ? toHex(val) : String(val)
  }

  console.log(`✅  Found ${Object.keys(tokens).length} variables.`)

  // ── Write globals.css ──────────────────────────────────────────────────────
  const cssVars = Object.entries(tokens)
    .map(([k, v]) => `  ${k}: ${v};`)
    .join('\n')

  const css = `/* AUTO-GENERATED by scripts/sync-figma-tokens.mjs — do not edit manually */
@tailwind base;
@tailwind components;
@tailwind utilities;

@import url('https://fonts.googleapis.com/css2?family=Figtree:ital,wght@0,300..900;1,300..900&family=Plus+Jakarta+Sans:ital,wght@0,200..800;1,200..800&display=swap');

:root {
${cssVars}

  /* Typography */
  --font-display: 'Plus Jakarta Sans', sans-serif;
  --font-body:    'Figtree', sans-serif;
}

*, *::before, *::after { box-sizing: border-box; }

body {
  margin: 0;
  font-family: var(--font-body);
  color: var(--color-text-primary, #040966);
  background: var(--color-bg-page, #ffffff);
  -webkit-font-smoothing: antialiased;
}
`

  const stylesDir = join(ROOT, 'src', 'styles')
  mkdirSync(stylesDir, { recursive: true })
  writeFileSync(join(stylesDir, 'globals.css'), css)
  console.log('📝  Wrote src/styles/globals.css')

  // ── Write tokens.ts ────────────────────────────────────────────────────────
  const tsContent = `// AUTO-GENERATED by scripts/sync-figma-tokens.mjs — do not edit manually
export const rawTokens = ${JSON.stringify(tokens, null, 2)} as const
export type RawTokens = typeof rawTokens
`
  const tokensDir = join(ROOT, 'src', 'tokens')
  mkdirSync(tokensDir, { recursive: true })
  writeFileSync(join(tokensDir, 'raw.ts'), tsContent)
  console.log('📝  Wrote src/tokens/raw.ts')

  console.log('🎉  Figma token sync complete.')
}

function writeFallback() {
  console.log('ℹ️   Fallback: tokens already committed in src/tokens/tokens.ts — nothing to update.')
}

main().catch(err => {
  console.error('❌  Sync failed:', err)
  process.exit(1)
})
